// ===== دوال مساعدة =====
export const getColor = (color: string, shade: string = 'main') => {
  const colorTokens = designTokens.colors as any;
  return colorTokens[color]?.[shade] || colorTokens.neutral[500];
};

export const getSpacing = (size: string) => {
  return designTokens.spacing[size as keyof typeof designTokens.spacing] || '1rem';
};

export const getShadow = (size: string) => {
  return designTokens.shadows[size as keyof typeof designTokens.shadows] || designTokens.shadows.md;
};
