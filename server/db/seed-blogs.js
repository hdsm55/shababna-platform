async function seedBlogs() {
    try {
        console.log('🌱 بدء إضافة مقالات المدونة...');

        for (const blog of blogs) {
            await query(
                `INSERT INTO blogs (title, content, author, image_url, created_at, updated_at)
         VALUES ($1, $2, $3, $4, NOW(), NOW())`,
                [blog.title, blog.content, blog.author, blog.image_url]
            );
            console.log(`✅ تم إضافة مقال: ${blog.title}`);
        }

        console.log('🎉 تم إضافة جميع مقالات المدونة بنجاح!');
    } catch (error) {
        console.error('❌ خطأ في إضافة مقالات المدونة:', error);
    }
}

